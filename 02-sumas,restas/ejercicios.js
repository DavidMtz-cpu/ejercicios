"use strict";
(() => {
    function sumar(number1, number2) {
        let resultado;
        resultado = number1 + number2;
        return resultado;
    }
    console.log(sumar(10, 15));
})();
(() => {
    function restar(number1, number2) {
        let resultado;
        resultado = number1 - number2;
        return resultado;
    }
    console.log(restar(10, 2));
})();
(() => {
    function multipiclar(number1, number2) {
        let resultado;
        resultado = number1 * number2;
        return resultado;
    }
    console.log(multipiclar(2, 10));
})();
(() => {
    function dividir(number1, number2) {
        let resultado;
        resultado = number1 / number2;
        return resultado;
    }
    console.log(dividir(2, 10));
})();
